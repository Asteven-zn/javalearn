package com.zn.operator;

public class Demo01 {
    public static void main(String[] args) {
        // 二元运算符
        // Ctrl + D ： 复制当前行到下一行
        int a = 10;
        int b = 20;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println((double) a/b);
    }

}
